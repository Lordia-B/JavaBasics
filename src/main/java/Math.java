public class Math {
    int a;
    int b;

    public Math(int x, int y){
         a = x;
         b = y;
    }

    public int add(){
        return a +b;
    }
    public int divide(){
        return a/b;
    }
    public int multiply(){
        return a * b;
    }
    public int subtract(){
        return a - b;
    }
}
